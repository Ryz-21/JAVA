public class Ejercicio9 {
    public static void main(String[] args) {

        //operadores logicos
        int x = 4;
        //operador and
        System.out.println(x < 5 && x < 10); // x es menor a 5 y menor a 10
        //operador O
        System.out.println(x < 5 || x < 4);

        //not operator
        System.out.println(!true);
    }
}
